using System;
using Xenko.Engine;
using Xenko.Games;

namespace VRSandbox
{
    public abstract class VRSandboxGame : Game
    {
        public VRSandboxGame() : base()
        {
            
        }

        protected override void Initialize()
        {
            base.Initialize();
        }

        protected override void Update(GameTime gameTime)
        {
            base.Update(gameTime);
        }

        protected override void Draw(GameTime gameTime)
        {
            base.Draw(gameTime);
        }
    }
}
